package lenguajeJava.valoresLiterales;

public class ValoresLiterales {
    public static void main (String[] args){
        // Literal de tipo char
        char c = 'A';
        System.out.println(c);

        //Literal de tipo int
        int i = 123;
        System.out.println(i);

        // Literal de tipo boolean (true o false)
        boolean b = true;
        System.out.println(b);

        // Literal de tipo double
        double d = 3.14;
        System.out.println(d);

        // Literal de tipo String(aunque no es de tipo primitivo)
        String s = "Hola";
        System.out.println(s);

        // Literal de tipo float: es decimal y lleva el sufijo f
        float f = 3.14f;
        System.out.println(f);

        // Valor entero expresado en binario
        int bin = 0b1001101;
        System.out.println(bin); // Salida: 77

    }
}
